    <ul>
<li>In Nepal, heavy, annual monsoon rainfall can be a flood risk. We have tried and tested a range of river monitoring technologies, from basic watchtowers to <a href="https://practicalaction.org/knowledge-centre/resources/monitoring-rivers-for-flood-early-warning-nepals-ingenious-solutions/">LiDAR sensors</a>. We also worked with the national government to develop a forecasting methodology that can <a href="https://floodresilience.net/resources/item/community-based-early-warning-systems-for-flood-risk-mitigation-in-nepal/">increase warning lead times by up to 6 hours</a>.</li>
<li>In Peru, where people are at risk from <em>huaycos </em>(flash floods or mudslides triggered by heavy downpours) we have developed low- and high-tech solutions: rainfall monitoring technology using basic rain gauges made from <a href="https://floodresilience.net/blogs/how-can-participatory-monitoring-help-us-better-understand-rainfall/">plastic bottles</a> are low cost and easily accessible; meanwhile <a href="https://practicalaction.org/knowledge-centre/resources/monitoring-rainfall-for-early-warning-perus-ingenious-solutions/">3D printed weather monitoring stations</a> improve the accuracy of early warnings and gather data to improve knowledge.</li>
<li>In Bangladesh, we’re using multiple methods to make sure warnings are accessible to more people. For example, <a href="https://floodresilience.net/resources/item/practical-actions-approach-in-disseminating-localized-flood-early-warning-in-bangladesh/">localised voice messaging</a> has been used to warn people when there’s a risk of flooding, and also to provide guidance on limiting the spread of COVID-19.</li>
<li>During flooding incidents in Thiès, Senegal, we <a href="https://floodresilience.net/resources/item/strengthening-climate-information-services-and-early-warning-systems-in-senegal-learning-from-the-2020-floods-in-thies/">identified gaps in the existing EWS</a>. To improve its effectiveness, we have established EWS ‘committees’ who now lead on disseminating warning messages to communities at risk.</li>
<li>In India, with our partners in the <a href="https://practicalaction.org/our-work/projects/understand-landslides-build-resilience/">LANDSLIP</a> research programme, we developed a prototype landslide EWS. This has led to a decision to develop a National Landslide Forecasting Centre.</li>
